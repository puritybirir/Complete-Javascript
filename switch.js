// Switch statement

var job = 'teacher';
var firstName = 'Birir'
switch (job) {
    case 'teacher':
    console.log(firstName + ' teaches kids how to code');
    break;
    case 'driver':
    console.log(firstName + ' drives an uber in Lisbon.');
    break;
    case 'designer':
    console.log(firstName + 'designs beautiful websites');
    break;
    default:
    console.log(firstName + ' does something else')
}